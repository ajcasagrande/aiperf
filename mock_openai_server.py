@app.post("/v1/chat/completions")
async def create_chat_completion(
    request: ChatCompletionRequest, token: str = Depends(verify_token)
):
    await simulate_delay()

    if should_error():
        raise HTTPException(status_code=500, detail="Simulated server error")

    request_dict = request.dict()

    # If streaming is requested
    if request.stream:
        return Response(
            content=stream_chat_completion(request_dict),
            media_type="text/event-stream",
        )

    # Get a mock response
    mock_content = generate_mock_response("chat")
    input_messages = " ".join([m.content for m in request.messages])

    # Calculate approximate token counts
    prompt_tokens = calculate_tokens(input_messages)
    completion_tokens = calculate_tokens(mock_content)
    total_tokens = prompt_tokens + completion_tokens

    response = {
        "id": f"chatcmpl-{uuid.uuid4().hex}",
        "object": "chat.completion",
        "created": int(time.time()),
        "model": request.model,
        "choices": [
            {
                "index": 0,
                "message": {
                    "role": "assistant",
                    "content": mock_content,
                },
                "finish_reason": "stop",
            }
        ],
        "usage": {
            "prompt_tokens": prompt_tokens,
            "completion_tokens": completion_tokens,
            "total_tokens": total_tokens,
        },
    }

    log_request("/v1/chat/completions", request_dict, response)
    return response


async def stream_chat_completion(request_dict):
    """Generate streaming response for chat completion."""
    model = request_dict.get("model", "gpt-3.5-turbo")
    response_id = f"chatcmpl-{uuid.uuid4().hex}"
    created = int(time.time())

    # Get a mock response
    mock_content = generate_mock_response("chat")

    # Break the response into words
    words = mock_content.split()
    chunk_size = random.randint(1, 3)  # Random number of words per chunk

    # Generate SSE response chunks
    for i in range(0, len(words), chunk_size):
        chunk = " ".join(words[i : i + chunk_size])

        response_chunk = {
            "id": response_id,
            "object": "chat.completion.chunk",
            "created": created,
            "model": model,
            "choices": [
                {
                    "index": 0,
                    "delta": {
                        "content": chunk,
                    },
                    "finish_reason": None,
                }
            ],
        }

        # If this is the last chunk, set finish_reason
        if i + chunk_size >= len(words):
            response_chunk["choices"][0]["finish_reason"] = "stop"

        yield f"data: {json.dumps(response_chunk)}\n\n"

        # Simulate token generation rate
        tokens_in_chunk = calculate_tokens(chunk)
        delay = tokens_in_chunk / SETTINGS["token_rate"]
        await asyncio.sleep(delay)

    # End of stream
    yield "data: [DONE]\n\n"


