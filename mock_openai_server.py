# Helper functions
async def simulate_delay():
    """Simulate a realistic API delay."""
    delay = random.uniform(SETTINGS["delay_min"], SETTINGS["delay_max"])
    await asyncio.sleep(delay)


def should_error():
    """Determine if this request should return an error based on error rate."""
    return random.random() < SETTINGS["error_rate"]


def generate_mock_response(prompt_type="chat"):
    """Generate a mock response based on the prompt type."""
    return random.choice(MOCK_RESPONSES[prompt_type])


def calculate_tokens(text):
    """Calculate token count (very rough approximation)."""
    # This is a very simple approximation (about 4 chars per token)
    return len(text) // 4


def log_request(endpoint, request_data, response_data):
    """Log request and response for later analysis."""
    REQUEST_LOG.append(
        {
            "timestamp": time.time(),
            "endpoint": endpoint,
            "request": request_data,
            "response": response_data,
        }
    )


# Authentication middleware
async def verify_token(authorization: Optional[str] = Header(None)):
    """Simulate API key verification."""
    if authorization is None:
        # We'll accept missing auth for easier testing
        return "mock_session"

    if not authorization.startswith("Bearer "):
        raise HTTPException(status_code=401, detail="Invalid authentication format")

    # In a real server, we'd validate the token here
    # For mock purposes, we'll accept any token
    token = authorization.replace("Bearer ", "")
    if token == "invalid_token":
        raise HTTPException(status_code=401, detail="Invalid API key")

    return token


